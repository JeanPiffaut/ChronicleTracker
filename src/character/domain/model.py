class Character:
    def __init__(self, character_id, name, description, year_of_birth, month_of_birth, day_of_birth, status, gender,
                 life_status):
        self.id = character_id
        self.name = name
        self.description = description
        self.year_of_birth = year_of_birth
        self.month_of_birth = month_of_birth
        self.day_of_birth = day_of_birth
        self.status = status
        self.gender = gender
        self.life_status = life_status
